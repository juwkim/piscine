int	is_upper(char c)
{
	return (('A' <= c) && (c <= 'Z'));
}

char	*ft_strlowcase(char *str)
{
	char	*p;

	p = str;
	while (*p)
	{
		if (is_upper(*p))
			*p ^= 32;
		p++;
	}
	return (str);
}
