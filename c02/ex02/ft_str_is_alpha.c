int	is_alpha(char c)
{
	c |= 32;
	return (('a' <= c) && (c <= 'z'));
}

int	ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if (!is_alpha(*str++))
			return (0);
	}
	return (1);
}
