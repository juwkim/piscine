int	is_lower(char c)
{
	return (('a' <= c) && (c <= 'z'));
}

char	*ft_strupcase(char *str)
{
	char	*p;

	p = str;
	while (*p)
	{
		if (is_lower(*p))
			*p ^= 32;
		p++;
	}
	return (str);
}
