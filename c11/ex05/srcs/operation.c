int	operation_add(int a, int b)
{
	return (a + b);
}

int	operation_minus(int a, int b)
{
	return (a - b);
}

int	operation_devide(int a, int b)
{
	return (a / b);
}

int	operation_multiply(int a, int b)
{
	return (a * b);
}

int	operation_modulo(int a, int b)
{
	return (a % b);
}
